
-- 1/ Tạo DB + Sử dụng DB
	CREATE DATABASE DB;
    
	USE DB;
	
-- 2/ Tạo bảng + Khoá chính
	CREATE TABLE GIAOVIEN
	(
		MAGV CHAR(3),
		HOTEN VARCHAR(50),
		LUONG FLOAT,
		PHAI CHAR(3),
		NGSINH DATE,
		DIACHI VARCHAR(50),
		GVQLCM CHAR(3),
		MABM CHAR(4),
		PRIMARY KEY(MAGV)
	);
    
	CREATE TABLE BOMON
	(
		MABM CHAR(4),
		TENBM VARCHAR(50),
		PHONG CHAR(3),
		DIENTHOAI CHAR(11),
		TRUONGBM CHAR(3),
		MAKHOA CHAR(4),
		NGAYNHANCHUC DATE,
		PRIMARY KEY(MABM)
	);
    
	CREATE TABLE KHOA
	(
		MAKHOA CHAR(4),
		TENKHOA VARCHAR(50),
		NAMTL INT,
		PHONG CHAR(3),
		DIENTHOAI CHAR(10),
		TRUONGKHOA CHAR(3),
		NGAYNHANCHUC DATE,
		PRIMARY KEY(MAKHOA)
	);
	
    CREATE TABLE CHUDE
	(
		MACD CHAR(4),
		TENCD VARCHAR(30),
		PRIMARY KEY(MACD)
	);
    
	CREATE TABLE DETAI
	(
		MADT CHAR(4),
		TENDT VARCHAR(50),
		CAPQL VARCHAR(20),
		KINHPHI FLOAT,
		NGAYBD DATE,
		NGAYKT DATE,
		MACD CHAR(4),
		GVCNDT CHAR(3),
		PRIMARY KEY(MADT)
	);
    
	CREATE TABLE CONGVIEC 
	(
		MADT CHAR(4),
		STT INT,
		TENCV VARCHAR(50),
		NGAYBD DATE,
		NGAYKT DATE,
		PRIMARY KEY(MADT,STT)
	);
	
	CREATE TABLE THAMGIADT
	(
		MAGV CHAR(3),
		MADT CHAR(4),
		STT INT,
		PHUCAP FLOAT,
		KETQUA VARCHAR(10),
		PRIMARY KEY(MAGV,MADT,STT)
	);
    
	CREATE TABLE GV_DT
	(
		MAGV CHAR(3),
		DIENTHOAI CHAR(10),
		PRIMARY KEY(MAGV,DIENTHOAI)
	);
    
	CREATE TABLE NGUOITHAN
	(
		MAGV CHAR(3),
		TEN VARCHAR(12),
		NGSINH DATE,
		PHAI CHAR(3),
		PRIMARY KEY(MAGV,TEN)
	);
	
